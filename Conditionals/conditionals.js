// if statement
let x = 5;
if (x > 0) {
    console.log("x is positive");
} else if (x === 0) {
    console.log("x is zero");
} else {
    console.log("x is negative");
}

// for loop
const fruits = ["apple", "banana", "cherry"];
for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i]);
}

// while loop
let count = 0;
while (count < 5) {
    console.log(count);
    count++;
}
